//operadores de comparação
#include <stdio.h>
int main() {
    int a = 5, b = 10;
    printf("a == b: %d\n", a == b); //0 (falso)
    printf("a != b: %d\n", a != b); //1 (verdadeiro)
    printf("a > b: %d\n", a > b); //0 (falso)
    printf("a < b: %d\n", a < b); //1 (verdadeiro)
    printf("a >= b: %d\n", a >= b); //0 (falso)
    printf("a <= b: %d\n", a <= b); //1 (verdadeiro)

    return 0;
}